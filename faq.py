    def __init__(self, questions, answer_id, answer):
        self.questions = questions
        self.answer_id = answer_id
        self.answer = answer      

    def get_questions(self):
        return self.questions
    
    def get_answer_id(self):
        return self.get_answer_id
    
    def get_answer(self):
        return self.answer